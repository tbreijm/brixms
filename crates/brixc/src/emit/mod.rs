//! The `emit` stage: Core IR + plan → a generated Rust cargo workspace.
//!
//! Approach (OWNER.md contract, Ring0_Build_Plan §1.8): **quote + prettyplease
//! into a generated cargo workspace, one module per relation (store + indices)
//! and per rule (one delta fn per delta source), with `#[deny(...)]` determinism
//! headers.** `brix explain --rust <Rule>` prints the module for one rule; the
//! flagship's whole generated workspace is an `insta` snapshot so codegen drift
//! is visible in review (§1.8).
//!
//! Pass 1 (this stage) is where *all* semantics live (Part XXVIII §28.1):
//! backends, opt levels, and targets change cost, never any observable value. So
//! the generated code is a faithful, boring translation — the determinism header
//! is what mechanically holds the line (no `HashMap`, no floats off the strict
//! path, etc.) inside generated crates too.
//!
//! ## Status
//!
//! The real column/row monomorphization consumes `brix-ir`'s typed nodes, which
//! land in a sibling lane. Until then this stage operates on the small
//! [`RelationDesc`]/[`RuleDesc`] descriptors below — enough to exercise and
//! snapshot the codegen *shape* (module layout, naming, the deny header, the
//! delta-fn-per-source structure) end to end with `quote`/`prettyplease`. When
//! brix-ir merges, the descriptors are populated from IR instead of by hand and
//! the token-emitting bodies fill in; the module boundaries do not move.

